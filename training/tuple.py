# Tuple having integers
my_tuple = (1, 2, 3)
print(my_tuple)

# tuple with mixed datatypes
my_tuple = (1, "Hello", 3.4)
print(my_tuple)

# Accessing tuple elements using slicing
my_tuple = ('p','r','o','g','r','a','m')

print(my_tuple[1:4])

# Concatenation
print((1, 2, 3) + (4, 5, 6))

my_tuple = ('a', 'p', 'p', 'l', 'e',)

print(my_tuple.count('p'))  
print(my_tuple.index('l'))  